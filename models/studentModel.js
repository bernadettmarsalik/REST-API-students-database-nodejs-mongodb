const mongoose = require("mongoose");

const studentSchema = mongoose.Schema({
  first_name: {
    type: String,
    required: true,
  },
  last_name: {
    type: String,
    required: true,
  },
  dob: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
    required: true,
  },
});

// exporting it we name it Hero and apply studentSchema
module.exports = mongoose.model("Student", studentSchema);
